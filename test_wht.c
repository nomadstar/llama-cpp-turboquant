    float T[128][128];
    for(int j=0; j<128; j++) {
        float e[128] = {0};
        e[j] = 1.0f;
        turbo_rotate_forward(e);
        // The output e is the j-th column of T
        for(int i=0; i<128; i++) {
            T[i][j] = e[i];
        }
    }
    
